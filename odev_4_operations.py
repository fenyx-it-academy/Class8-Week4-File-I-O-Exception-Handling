import math



def cikar(number_1, number_2):

    answer = math.ceil(number_1-number_2)
    return answer
    
def toplam(number_1, number_2):

    answer = math.ceil(number_1+number_2)
    return answer
def bol(number_1, number_2):

    answer = math.ceil(number_1/number_2)
    return answer
def carpi(number_1, number_2):

    answer = math.ceil(number_1*number_2)
    return answer



    
    